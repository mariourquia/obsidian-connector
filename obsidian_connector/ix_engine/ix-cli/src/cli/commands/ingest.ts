/** Normalise user-supplied language names to their canonical enum values. */
const LANG_ALIASES: Record<string, string> = {
  'c++': 'cpp',
  'cplusplus': 'cpp',
  'c#': 'csharp',
  'cs': 'csharp',
  'js': 'javascript',
  'ts': 'typescript',
  'py': 'python',
  'rb': 'ruby',
  'rs': 'rust',
  'kt': 'kotlin',
};

function parseLangs(raw: string): Set<string> {
  return new Set(
    raw.split(',')
      .map(s => s.trim().toLowerCase())
      .map(s => LANG_ALIASES[s] ?? s)
      .filter(Boolean),
  );
}

