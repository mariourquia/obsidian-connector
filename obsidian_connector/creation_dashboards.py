def generate_project_one_pager(
    vault: "str | Path",
    project: "str",
    *,
    now_iso: str,
    dry_run: bool = False,
) -> dict:
    """Scaffold ``Projects/{Project}/Project One-Pager.md`` with prose fences.

    ONLY creates the file when absent.  Never overwrites existing prose.

    Returns ``{path, dry_run, created}`` where ``created`` is False when the
    file already existed and was left untouched.
    """
    from obsidian_connector import creation_projects as cp

    vault_path = Path(vault)
    proj_obj = cp.get_project(vault_path, project)
    if proj_obj is None:
        return {"path": None, "dry_run": dry_run, "error": f"project not found: {project}"}

    path = _project_dir(vault_path, proj_obj.name) / "Project One-Pager.md"

    if path.exists():
        rel = str(path.relative_to(vault_path))
        return {"path": rel, "dry_run": dry_run, "created": False}

    fm = _fm({
        "type": "project-one-pager",
        "project": proj_obj.name,
        "created_at": now_iso,
    })

    def _prose_fence(field: str, placeholder: str) -> str:
        return _fence(f"one-pager-{field}", placeholder, None)

    content = (
        f"{fm}\n\n"
        f"# {proj_obj.name} — One-Pager\n\n"
        f"## Goal\n\n{_prose_fence('goal', '_Describe the project goal in one sentence._')}\n\n"
        f"## Intent\n\n{_prose_fence('intent', '_What does success look like?_')}\n\n"
        f"## Target users\n\n{_prose_fence('target-users', '_Who uses this?_')}\n\n"
        f"## Architecture\n\n{_prose_fence('architecture', '_Key technical decisions._')}\n\n"
        f"## Why now\n\n{_prose_fence('why', '_Why prioritize this now?_')}\n"
    )

    rel = _safe_write(path, content, vault_path, dry_run)
    return {"path": rel, "dry_run": dry_run, "created": True}


