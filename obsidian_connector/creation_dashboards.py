def _project_dir(vault_path: Path, project_name: str) -> Path:
    return vault_path / "Projects" / project_name


def _safe_write(path: Path, content: str, vault_path: Path, dry_run: bool) -> str:
    """Write *content* to *path* via atomic_write unless dry_run."""
    from obsidian_connector.write_manager import atomic_write
    from obsidian_connector.audit import log_action

    rel = str(path.relative_to(vault_path))
    if dry_run:
        return rel
    path.parent.mkdir(parents=True, exist_ok=True)
    atomic_write(path, content, vault_root=vault_path, tool_name="creation_dashboards")
    log_action("creation_dashboards", "write", rel)
    return rel


