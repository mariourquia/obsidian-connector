def generate_project_dashboard(
    vault: "str | Path",
    project: "str",
    *,
    now_iso: str,
    github_root: "str | Path | None" = None,
    dry_run: bool = False,
    runner: "Callable | None" = None,
) -> dict:
    """Generate ``Projects/{Project}/Project Dashboard.md``.

    Layout:
    header (name + goal/intent) → Next lead → state badges → Repos table →
    Top backlog → Load for an agent → links.

    Returns ``{path, dry_run}``.
    """
    from obsidian_connector import creation_projects as cp
    from obsidian_connector import creation_repo_status as crs
    from obsidian_connector import creation_next as cn
    from obsidian_connector import creation_backlog as cb

    vault_path = Path(vault)
    gh_root = Path(github_root) if github_root else Path.home() / "dev"
    runner_kwargs: dict = {}
    if runner is not None:
        runner_kwargs["runner"] = runner

    proj_obj = cp.get_project(vault_path, project)
    if proj_obj is None:
        return {"path": None, "dry_run": dry_run, "error": f"project not found: {project}"}

    prose = cp.read_one_pager_prose(vault_path, proj_obj)
    goal = prose.get("goal") or prose.get("intent") or "_no one-pager yet_"

    # --- top next action for this project ------------------------------------
    recs = cn.next_actions(
        vault_path,
        scope="project",
        project=proj_obj.slug,
        github_root=gh_root,
        now_iso=now_iso,
        limit=3,
        **runner_kwargs,
    )

    if recs:
        top = recs[0]
        reasons = "; ".join(str(r) for r in (top.get("reason") or [])[:2])
        next_lead = f"**→ Next:** {top['action']}"
        if reasons:
            next_lead += f"  _({reasons})_"
    else:
        next_lead = "**→ Next:** _no actionable items_"

    # --- repo statuses -------------------------------------------------------
    repo_entries = cp.project_repo_entries(vault_path, proj_obj)
    repo_statuses: list["crs.RepoStatus"] = []
    for entry in repo_entries:
        try:
            rs = crs.repo_status(
                entry,
                github_root=gh_root,
                now_iso=now_iso,
                with_prs=False,
                with_tests=False,
                with_build=False,
                **runner_kwargs,
            )
            repo_statuses.append(rs)
        except Exception:
            pass

    # State badges summary
    n_decision = sum(1 for rs in repo_statuses if rs.classification == "blocked-by-decision")
    n_stale = sum(1 for rs in repo_statuses if rs.classification in ("stale", "dormant"))
    n_blocked = sum(1 for rs in repo_statuses if "blocked" in rs.classification)
    n_clean = sum(1 for rs in repo_statuses if rs.classification in ("clean-and-ready", "ready-for-next-agent"))

    badge_parts: list[str] = []
    if n_decision:
        badge_parts.append(f"{n_decision} needs-decision")
    if n_stale:
        badge_parts.append(f"{n_stale} stale")
    if n_blocked:
        badge_parts.append(f"{n_blocked} blocked")
    if n_clean:
        badge_parts.append(f"{n_clean} clean")
    badges_line = "  ".join(f"[{b}]" for b in badge_parts) if badge_parts else "[no repos]"

    # Repos table
    repo_table_rows: list[list[str]] = []
    for rs in repo_statuses:
        branch = rs.branch or "?"
        head = rs.head[:7] if rs.head else "?"
        badge = _state_badge(rs.classification)
        next_a = (rs.next_action[:50] + "...") if len(rs.next_action) > 50 else rs.next_action
        repo_table_rows.append([rs.display_name, f"{branch}@{head}", badge, next_a])

    repos_table = _table(["Repo", "Branch", "State", "Next"], repo_table_rows)

    # Top backlog
    backlog = cb.list_backlog(vault_path, project=proj_obj.slug)
    backlog_lines: list[str] = []
    for item in backlog[:10]:
        pri = item.get("priority", "?")
        status = item.get("status", "?")
        title = item.get("title", item["id"])
        bid = item["id"]
        flags = ""
        if item.get("needs_decision"):
            flags += " [needs-decision]"
        if status == "blocked":
            flags += " [blocked]"
        backlog_lines.append(f"- `{pri}` `{status}` {title}{flags}  `{bid}`")

    if not backlog_lines:
        backlog_lines = ["_No backlog items._"]

    # Load for an agent section
    one_pager_link = f"[[Projects/{proj_obj.name}/Project One-Pager]]"
    repo_links = "  ".join(
        f"[[Projects/{proj_obj.name}/Repos/{r}]]" for r in proj_obj.repos
    )
    agent_load = (
        f"One-pager: {one_pager_link}\n\n"
        f"Repo views: {repo_links}\n\n"
        "_Context pack: not yet generated._"
    )

    # --- assemble -----------------------------------------------------------
    fm = _fm({
        "type": "project-dashboard",
        "project": proj_obj.name,
        "last_sync": now_iso,
        "generated_by": "creation_dashboards",
    })

    n_repos = len(proj_obj.repos)
    content = (
        f"{fm}\n\n"
        f"{_GENERATED_BANNER}\n\n"
        f"# {proj_obj.name}\n\n"
        f"_{goal}_\n\n"
        f"{next_lead}\n\n"
        f"{badges_line}\n\n"
        f"## Repos ({n_repos})\n\n"
        f"{repos_table}\n\n"
        f"## Top backlog (ranked)\n\n"
        + "\n".join(backlog_lines)
        + f"\n\n## Load for an agent\n\n{agent_load}\n\n"
        "---\n\n"
        f"**Links:** [[Dashboard]] · [[Projects]] · [[Projects/{proj_obj.name}/Project One-Pager]]\n"
    )

    path = _project_dir(vault_path, proj_obj.name) / "Project Dashboard.md"
    rel = _safe_write(path, content, vault_path, dry_run)
    return {"path": rel, "dry_run": dry_run}


