def generate_next_actions(
    vault: "str | Path",
    *,
    now_iso: str,
    github_root: "str | Path | None" = None,
    dry_run: bool = False,
    runner: "Callable | None" = None,
    limit: int = 20,
) -> dict:
    """Generate ``Next Actions.md`` — ranked recommendations with reasoning.

    Each item shows: rank, action, project·repo, confidence, reason chips,
    flags, suggested workflow, context-pack link.  Footer explains the
    transparent scoring formula + that weights are user-editable.

    Returns ``{path, dry_run}``.
    """
    from obsidian_connector import creation_next as cn

    vault_path = Path(vault)
    gh_root = Path(github_root) if github_root else Path.home() / "dev"
    runner_kwargs: dict = {}
    if runner is not None:
        runner_kwargs["runner"] = runner

    recs = cn.next_actions(
        vault_path,
        scope="global",
        github_root=gh_root,
        now_iso=now_iso,
        limit=limit,
        **runner_kwargs,
    )

    lines: list[str] = []
    for rank, rec in enumerate(recs, 1):
        proj = rec.get("project") or ""
        repo = rec.get("repo") or ""
        loc = " · ".join(x for x in [proj, repo] if x)
        reasons = rec.get("reason") or []
        reason_chips = "  ".join(f"`{r}`" for r in reasons[:3])
        confidence = rec.get("confidence", 0.0)
        conf_str = f"{confidence:.0%}"
        action = rec.get("action", "")

        flags: list[str] = []
        if rec.get("requires_mario_decision"):
            flags.append("[needs-decision]")
        # detect blocked from reasons
        if any("blocked" in str(r).lower() for r in reasons):
            flags.append("[blocked]")

        workflow = rec.get("suggested_workflow")
        ctx_pack = rec.get("context_pack")

        item_lines = [f"### {rank}. {action}"]
        if loc:
            item_lines.append(f"**Project · Repo:** {loc}")
        item_lines.append(f"**Confidence:** {conf_str}")
        if reason_chips:
            item_lines.append(f"**Reasons:** {reason_chips}")
        if flags:
            item_lines.append("**Flags:** " + "  ".join(flags))
        if workflow:
            item_lines.append(f"**Suggested workflow:** {workflow}")
        if ctx_pack:
            item_lines.append(f"**Context pack:** {ctx_pack}")
        lines.append("\n".join(item_lines))

    if not lines:
        lines = ["_No recommendations generated._"]

    fm = _fm({
        "type": "next-actions",
        "last_sync": now_iso,
        "generated_by": "creation_dashboards",
    })

    weights_path = "creation/dashboard-weights.json"
    footer = (
        "---\n\n"
        "**Scoring formula:** `score = sum(weight[k] * signal[k])` where signals are "
        "normalized 0-1 per factor (urgency, impact, dependency_unlock, stale_age, "
        "user_emphasis, deadline, unfinished_session, repo_readiness).  "
        f"Weights are user-editable in `{weights_path}` in the vault root.\n\n"
        "**Links:** [[Dashboard]] · [[Projects]] · [[Stale Context]] · [[Pending Decisions]]\n"
    )

    content = (
        f"{fm}\n\n"
        f"{_GENERATED_BANNER}\n\n"
        f"# Next Actions\n\n"
        f"_Last sync: {now_iso} · {len(recs)} recommendations_\n\n"
        + "\n\n".join(lines)
        + f"\n\n{footer}"
    )

    path = vault_path / "Next Actions.md"
    rel = _safe_write(path, content, vault_path, dry_run)
    return {"path": rel, "dry_run": dry_run}


