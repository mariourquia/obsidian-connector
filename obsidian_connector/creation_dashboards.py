# refresh_all — orchestrator
# ---------------------------------------------------------------------------

def refresh_all(
    vault: "str | Path",
    *,
    now_iso: str,
    github_root: "str | Path | None" = None,
    scope: "str | None" = None,
    dry_run: bool = False,
    runner: "Callable | None" = None,
) -> dict:
    """Regenerate the full global dashboard set.

    When *scope* names a project slug, also regenerates that project's
    drilldown dashboards (project dashboard + one-pager scaffold + repo views).
    When *scope* names a repo dir_name, regenerates the global set plus the
    containing project's and that repo's view.

    Parameters
    ----------
    vault:
        Vault root path.
    now_iso:
        ISO8601 timestamp (clock boundary: supplied by the caller).
    github_root:
        Root of local git repos (default: ``~/dev``).
    scope:
        Optional project slug or repo dir_name to additionally drill into.
    dry_run:
        When True, skip all writes and return the list of paths that
        *would* have been written.
    runner:
        Injectable subprocess runner for tests.

    Returns
    -------
    dict
        ``{written: list[str], dry_run: bool}``
    """
    from obsidian_connector import creation_projects as cp

    vault_path = Path(vault)
    gh_root = Path(github_root) if github_root else Path.home() / "dev"

    written: list[str] = []
    kw: dict = dict(now_iso=now_iso, github_root=gh_root, dry_run=dry_run, runner=runner)

    # Global set (always)
    for fn in [
        lambda: generate_global_dashboard(vault_path, **kw),
        lambda: generate_projects_index(vault_path, now_iso=now_iso, dry_run=dry_run),
        lambda: generate_next_actions(vault_path, **kw),
        lambda: generate_stale_context(vault_path, now_iso=now_iso, dry_run=dry_run),
        lambda: generate_pending_decisions(vault_path, now_iso=now_iso, dry_run=dry_run),
        lambda: generate_active_sessions(vault_path, now_iso=now_iso, dry_run=dry_run),
    ]:
        try:
            result = fn()
            if result.get("path"):
                written.append(result["path"])
        except Exception:
            pass

    if scope is None:
        return {"written": written, "dry_run": dry_run}

    # Resolve scope to a project (and optionally a repo)
    projects = cp.list_projects(vault_path)
    scoped_project: "cp.Project | None" = None
    scoped_repo: str | None = None

    # Check if scope is a project slug/name
    scoped_project = cp.get_project(vault_path, scope)
    if scoped_project is None:
        # Check if scope is a repo dir_name belonging to any project
        for proj in projects:
            if scope in proj.repos:
                scoped_project = proj
                scoped_repo = scope
                break

    if scoped_project is not None:
        # Project drilldowns
        try:
            r = generate_project_dashboard(vault_path, scoped_project.slug, **kw)
            if r.get("path"):
                written.append(r["path"])
        except Exception:
            pass
        try:
            r = generate_project_one_pager(vault_path, scoped_project.slug,
                                           now_iso=now_iso, dry_run=dry_run)
            if r.get("path"):
                written.append(r["path"])
        except Exception:
            pass

        # Repo views: if scoped to a repo, only that one; else all project repos
        repos_to_refresh = [scoped_repo] if scoped_repo else list(scoped_project.repos)
        for repo_name in repos_to_refresh:
            try:
                r = generate_repo_view(vault_path, scoped_project.slug, repo_name, **kw)
                if r.get("path"):
                    written.append(r["path"])
            except Exception:
                pass

    return {"written": written, "dry_run": dry_run}
