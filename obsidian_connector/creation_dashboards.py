def generate_repo_view(
    vault: "str | Path",
    project: "str",
    repo: "str",
    *,
    now_iso: str,
    github_root: "str | Path | None" = None,
    dry_run: bool = False,
    runner: "Callable | None" = None,
) -> dict:
    """Generate ``Projects/{Project}/Repos/{repo}.md``.

    Generated repo state lives inside a ``service:repo-status`` fence.
    User notes OUTSIDE the fence are preserved across regeneration.

    Returns ``{path, dry_run}``.
    """
    from obsidian_connector import creation_projects as cp
    from obsidian_connector import creation_repo_status as crs
    from obsidian_connector import creation_backlog as cb

    vault_path = Path(vault)
    gh_root = Path(github_root) if github_root else Path.home() / "dev"
    runner_kwargs: dict = {}
    if runner is not None:
        runner_kwargs["runner"] = runner

    proj_obj = cp.get_project(vault_path, project)
    if proj_obj is None:
        return {"path": None, "dry_run": dry_run, "error": f"project not found: {project}"}

    repo_entries = cp.project_repo_entries(vault_path, proj_obj)
    entry = next((e for e in repo_entries if e.dir_name == repo), None)
    if entry is None:
        return {"path": None, "dry_run": dry_run, "error": f"repo not found: {repo}"}

    try:
        rs = crs.repo_status(
            entry,
            github_root=gh_root,
            now_iso=now_iso,
            with_prs=True,
            with_tests=False,
            with_build=False,
            **runner_kwargs,
        )
    except Exception as exc:
        rs = None
        _rs_err = str(exc)

    # Read existing file (to preserve user notes outside the fence)
    repo_view_path = _project_dir(vault_path, proj_obj.name) / "Repos" / f"{repo}.md"
    existing: str | None = None
    if repo_view_path.exists():
        try:
            existing = repo_view_path.read_text(encoding="utf-8")
        except OSError:
            pass

    # Extract user notes that live OUTSIDE the service:repo-status fence
    user_notes = _extract_user_notes_outside_fence(existing, "repo-status")

    # Build the generated state body
    if rs is not None:
        classification = rs.classification
        badge = _state_badge(classification)

        git_lines = [
            f"- **Path:** `{rs.repo_path}`",
            f"- **Branch:** `{rs.branch}` @ `{rs.head}`",
            f"- **Dirty:** {rs.dirty}  **Untracked:** {rs.untracked}",
            f"- **vs origin:** {rs.ahead} ahead, {rs.behind} behind",
        ]
        if rs.recent_commits:
            git_lines.append("\n**Recent commits:**")
            for c in rs.recent_commits[:5]:
                git_lines.append(f"  - {c}")
        git_section = "\n".join(git_lines)

        # PRs section
        open_pr_lines: list[str] = []
        for pr in rs.open_prs:
            draft_flag = " [draft]" if pr.get("is_draft") else ""
            open_pr_lines.append(f"- #{pr.get('number', '?')} {pr.get('title', '')}{draft_flag}")
        if not open_pr_lines:
            open_pr_lines = ["_No open PRs._"]

        merged_pr_lines: list[str] = []
        for pr in rs.merged_prs_recent:
            merged_pr_lines.append(f"- #{pr.get('number', '?')} {pr.get('title', '')}")
        if not merged_pr_lines:
            merged_pr_lines = ["_No recently merged PRs._"]

        pr_section = (
            "**Open:**\n" + "\n".join(open_pr_lines)
            + "\n\n**Recently merged:**\n" + "\n".join(merged_pr_lines)
        )

        # Tests / build / deploy
        t_status = rs.tests.get("status", "unknown")
        t_summary = rs.tests.get("summary", "")
        b_status = rs.build.get("status", "unknown")
        d_status = rs.deploy.get("status", "unknown")
        tbd_section = (
            f"- **Tests:** {t_status}"
            + (f" — `{t_summary[:120]}`" if t_summary else "")
            + f"\n- **Build:** {b_status}"
            + f"\n- **Deploy:** {d_status}"
        )

        # Work context
        repo_backlog = cb.list_backlog(vault_path, project=proj_obj.slug)
        repo_items = [i for i in repo_backlog if repo in (i.get("repos") or [])]
        backlog_links: list[str] = []
        for item in repo_items[:5]:
            bid = item["id"]
            title = item.get("title", bid)
            backlog_links.append(f"- [[Backlog/{proj_obj.slug}/{bid}]] — {title}")

        blockers = list(rs.blockers)
        blocker_lines = [f"- {b}" for b in blockers] if blockers else ["_none_"]

        context_section = (
            f"**Blockers:**\n" + "\n".join(blocker_lines)
            + f"\n\n**Linked backlog:**\n"
            + ("\n".join(backlog_links) if backlog_links else "_none_")
            + f"\n\n**Suggested workflow:** _none configured_"
        )

        generated_body = (
            f"{badge}  **{classification}**\n\n"
            f"_Last sync: {now_iso}_\n\n"
            f"## Git\n\n{git_section}\n\n"
            f"## PRs\n\n{pr_section}\n\n"
            f"## Tests / build / deploy\n\n{tbd_section}\n\n"
            f"## Work context\n\n{context_section}"
        )
    else:
        generated_body = (
            f"[unknown]  **repo state unavailable**\n\n"
            f"_Last sync: {now_iso}_\n\n"
            f"_Error: {_rs_err}_"
        )

    # The fence wraps the generated region; user notes come after
    fence_block = _fence("repo-status", generated_body, existing)

    fm = _fm({
        "type": "repo-view",
        "project": proj_obj.name,
        "repo": repo,
        "last_sync": now_iso,
        "generated_by": "creation_dashboards",
    })

    # Preserve user notes that were originally outside (after) the fence
    user_section = ""
    if user_notes.strip():
        user_section = f"\n\n{user_notes.strip()}"

    content = (
        f"{fm}\n\n"
        f"{_GENERATED_BANNER}\n\n"
        f"# {entry.display_name}\n\n"
        f"{fence_block}"
        f"{user_section}\n"
    )

    rel = _safe_write(repo_view_path, content, vault_path, dry_run)
    return {"path": rel, "dry_run": dry_run}


def _extract_user_notes_outside_fence(existing: str | None, fence_name: str) -> str:
    """Return text that lives AFTER the service fence (user notes region).

    Strips any content that was generated by this module (frontmatter,
    banner, heading, the fence block itself) and returns only the trailing
    user-authored text.
    """
    if not existing:
        return ""
    begin_marker = f"<!-- service:{fence_name}:begin -->"
    end_marker = f"<!-- service:{fence_name}:end -->"
    b = existing.find(begin_marker)
    e = existing.find(end_marker)
    if b == -1 or e == -1:
        return ""
    after_fence = existing[e + len(end_marker):].strip()
    return after_fence


