def generate_projects_index(
    vault: "str | Path",
    *,
    now_iso: str,
    dry_run: bool = False,
) -> dict:
    """Generate ``Projects.md`` — an index of all projects.

    Returns ``{path, dry_run}``.
    """
    from obsidian_connector import creation_projects as cp
    from obsidian_connector import creation_backlog as cb

    vault_path = Path(vault)
    projects = cp.list_projects(vault_path)

    rows: list[list[str]] = []
    for proj in projects:
        backlog = cb.list_backlog(vault_path, project=proj.slug)
        n_open = sum(1 for i in backlog if i.get("status") not in ("done", "archived"))
        repos_str = ", ".join(proj.repos[:5])
        if len(proj.repos) > 5:
            repos_str += f" (+{len(proj.repos) - 5})"
        rows.append([
            f"[[Projects/{proj.name}/Project Dashboard|{proj.name}]]",
            proj.status,
            str(len(proj.repos)),
            str(n_open),
            repos_str,
        ])

    table = _table(["Project", "Status", "Repos", "Open items", "Repos list"], rows)

    fm = _fm({
        "type": "projects-index",
        "last_sync": now_iso,
        "generated_by": "creation_dashboards",
    })

    content = (
        f"{fm}\n\n"
        f"{_GENERATED_BANNER}\n\n"
        f"# Projects\n\n"
        f"_Last sync: {now_iso} · {len(projects)} projects_\n\n"
        f"{table}\n\n"
        "---\n\n"
        "**Links:** [[Dashboard]] · [[Next Actions]] · [[Stale Context]]\n"
    )

    path = vault_path / "Projects.md"
    rel = _safe_write(path, content, vault_path, dry_run)
    return {"path": rel, "dry_run": dry_run}


