# obsidian_connector/creation_dashboards.py
"""Fenced markdown dashboard generators for the Creation Vault OS (Task 5).

Generates materialized Obsidian markdown dashboards from the Project entity,
git+PR repo classifier, explainable next-action engine, backlog, freshness audit,
and session lifecycle.

Every write goes through ``write_manager.atomic_write``.
Fully-generated dashboards (Dashboard.md, Projects.md, Next Actions.md,
Stale Context.md, Pending Decisions.md, Active Sessions.md) are wholly
regenerated each run (carry ``last_sync`` + banner).

Hybrid files with user prose (repo views, one-pagers) use ``service:`` fences
to preserve hand-written content across regeneration.

Read-only seam: this module reads backlog/freshness/session; it never appends
events or mutates backlog.  No clock — ``now_iso`` is passed in.
"""
from __future__ import annotations

import re
from pathlib import Path
from typing import Any, Callable

# ---------------------------------------------------------------------------
# Fence markers (HTML comment style, matching creation_backlog convention)
# ---------------------------------------------------------------------------

_FENCE_PATTERN = re.compile(
    r"<!--\s*service:([^:]+):begin\s*-->(.*?)<!--\s*service:\1:end\s*-->",
    re.DOTALL,
)

_GENERATED_BANNER = (
    "<!-- generated by creation_dashboards — manual edits below "
    "service: fences are preserved; edits above are overwritten on refresh -->"
)


