def _fm(meta: dict) -> str:
    """Render a YAML frontmatter block from *meta*.

    Always includes ``last_sync`` (from ``meta["last_sync"]``).  The returned
    string includes the opening and closing ``---`` delimiters.
    """
    lines = ["---"]
    for key, val in meta.items():
        if isinstance(val, bool):
            lines.append(f"{key}: {str(val).lower()}")
        elif isinstance(val, (int, float)):
            lines.append(f"{key}: {val}")
        elif val is None:
            lines.append(f"{key}: null")
        elif isinstance(val, list):
            if not val:
                lines.append(f"{key}: []")
            else:
                lines.append(f"{key}:")
                for item in val:
                    lines.append(f"  - {item}")
        else:
            # strings — quote if they contain special chars
            s = str(val)
            if any(c in s for c in (':', '#', '[', ']', '{', '}', ',', '"', "'", '\n')):
                lines.append(f'{key}: "{s}"')
            else:
                lines.append(f"{key}: {s}")
    lines.append("---")
    return "\n".join(lines)


def _table(headers: list[str], rows: list[list[Any]]) -> str:
    """Render a Markdown pipe table.

    Parameters
    ----------
    headers:
        Column header strings.
    rows:
        List of rows, each a list of cell values (converted to str).

    Returns
    -------
    str
        A Markdown table string (no trailing newline).
    """
    if not headers:
        return ""
    col_widths = [len(h) for h in headers]
    str_rows = [[str(cell) for cell in row] for row in rows]
    for row in str_rows:
        for i, cell in enumerate(row):
            if i < len(col_widths):
                col_widths[i] = max(col_widths[i], len(cell))

    def _pad(s: str, width: int) -> str:
        return s + " " * (width - len(s))

    header_line = "| " + " | ".join(_pad(h, col_widths[i]) for i, h in enumerate(headers)) + " |"
    sep_line = "| " + " | ".join("-" * col_widths[i] for i in range(len(headers))) + " |"
    data_lines = []
    for row in str_rows:
        # Pad row to number of headers
        padded = list(row) + [""] * (len(headers) - len(row))
        data_lines.append(
            "| " + " | ".join(_pad(padded[i], col_widths[i]) for i in range(len(headers))) + " |"
        )
    return "\n".join([header_line, sep_line] + data_lines)


def _fence(name: str, body: str, existing: str | None) -> str:
    """Return a ``service:<name>`` fenced block.

    If *existing* already contains a ``service:<name>`` fence, the content
    inside it is preserved (the *body* argument is ignored).  This lets
    callers always pass the freshly-computed body while respecting any
    hand-written content the user has placed inside the fence.

    Parameters
    ----------
    name:
        Fence name (without ``service:`` prefix and without ``:begin``/``:end``).
    body:
        Default body to use when no existing fence content is found.
    existing:
        Full text of the file as it exists on disk (or None for a new file).

    Returns
    -------
    str
        The fenced block string, including begin/end markers.
    """
    begin = f"<!-- service:{name}:begin -->"
    end = f"<!-- service:{name}:end -->"

    if existing:
        pattern = re.compile(
            r"<!--\s*service:" + re.escape(name) + r":begin\s*-->(.*?)<!--\s*service:" + re.escape(name) + r":end\s*-->",
            re.DOTALL,
        )
        match = pattern.search(existing)
        if match:
            preserved = match.group(1).strip("\n")
            return f"{begin}\n{preserved}\n{end}"

    # No existing fence — emit the provided body
    body_stripped = body.strip("\n")
    if body_stripped:
        return f"{begin}\n{body_stripped}\n{end}"
    return f"{begin}\n{end}"


def _state_badge(classification: str) -> str:
    """Return a short emoji-free badge string for a classification."""
    _BADGES: dict[str, str] = {
        "clean-and-ready": "[clean]",
        "ready-for-next-agent": "[ready]",
        "mid-implementation": "[wip]",
        "waiting-on-pr-review": "[pr-review]",
        "blocked-by-tests": "[tests-fail]",
        "blocked-by-decision": "[needs-decision]",
        "needs-sync": "[needs-sync]",
        "behind": "[behind]",
        "ahead": "[ahead]",
        "dormant": "[dormant]",
        "stale": "[stale]",
        "unknown": "[unknown]",
    }
    return _BADGES.get(classification, f"[{classification}]")


# ---------------------------------------------------------------------------
