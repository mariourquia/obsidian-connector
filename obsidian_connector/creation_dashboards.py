def generate_global_dashboard(
    vault: "str | Path",
    *,
    now_iso: str,
    github_root: "str | Path | None" = None,
    dry_run: bool = False,
    runner: "Callable | None" = None,
) -> dict:
    """Generate ``Dashboard.md`` — the hybrid global triage console.

    Layout:
    1. ``## ▶ Do next`` — top 5 from ``next_actions(limit=5)``
    2. ``## Projects`` table — one row per project (state from repo_status)
    3. ``## Needs decision`` / ``## Stale`` / ``## Clean & ready`` rollups
    4. Links footer

    Returns ``{path, dry_run}``.
    """
    from obsidian_connector import creation_next as cn
    from obsidian_connector import creation_projects as cp
    from obsidian_connector import creation_repo_status as crs
    from obsidian_connector import creation_backlog as cb

    vault_path = Path(vault)
    gh_root = Path(github_root) if github_root else Path.home() / "dev"

    # --- top 5 next actions --------------------------------------------------
    runner_kwargs: dict = {}
    if runner is not None:
        runner_kwargs["runner"] = runner

    recs = cn.next_actions(
        vault_path,
        scope="global",
        github_root=gh_root,
        now_iso=now_iso,
        limit=5,
        **runner_kwargs,
    )

    do_next_lines: list[str] = []
    for rec in recs:
        proj = rec.get("project") or ""
        repo = rec.get("repo") or ""
        loc = " · ".join(x for x in [proj, repo] if x)
        reasons = rec.get("reason") or []
        reason_str = "; ".join(str(r) for r in reasons[:2])
        action = rec.get("action", "")
        flags = ""
        if rec.get("requires_mario_decision"):
            flags += " [needs-decision]"
        if rec.get("confidence", 1.0) < 0.3:
            flags += " [low-confidence]"
        line = f"- **{action}**{flags}"
        if loc:
            line += f" — {loc}"
        if reason_str:
            line += f"  _({reason_str})_"
        do_next_lines.append(line)

    if not do_next_lines:
        do_next_lines = ["_No actionable items found._"]

    # --- projects table ------------------------------------------------------
    projects = cp.list_projects(vault_path)

    table_rows: list[list[str]] = []
    needs_decision_names: list[str] = []
    stale_names: list[str] = []
    clean_names: list[str] = []

    for proj in projects:
        # Collect repo statuses for this project
        repo_entries = cp.project_repo_entries(vault_path, proj)
        statuses: list["crs.RepoStatus"] = []
        for entry in repo_entries:
            try:
                rs = crs.repo_status(
                    entry,
                    github_root=gh_root,
                    now_iso=now_iso,
                    with_prs=False,
                    with_tests=False,
                    with_build=False,
                    **runner_kwargs,
                )
                statuses.append(rs)
            except Exception:
                pass

        # Rollup classification: worst-case wins
        _PRIORITY_ORDER = [
            "needs-sync", "blocked-by-tests", "waiting-on-pr-review",
            "mid-implementation", "behind", "ahead", "blocked-by-decision",
            "stale", "dormant", "clean-and-ready", "ready-for-next-agent", "unknown",
        ]
        def _cls_rank(c: str) -> int:
            try:
                return _PRIORITY_ORDER.index(c)
            except ValueError:
                return len(_PRIORITY_ORDER)

        worst_cls = "unknown"
        worst_next = ""
        if statuses:
            worst = min(statuses, key=lambda r: _cls_rank(r.classification))
            worst_cls = worst.classification
            worst_next = worst.next_action or ""

        badge = _state_badge(worst_cls)

        # Flags: decision / stale
        all_backlog = cb.list_backlog(vault_path, project=proj.slug)
        has_decision = any(i.get("needs_decision") for i in all_backlog)
        n_stale = sum(1 for rs in statuses if rs.classification in ("stale", "dormant"))
        flag_parts: list[str] = []
        if has_decision:
            flag_parts.append("decision")
        if n_stale:
            flag_parts.append(f"{n_stale} stale")
        flags_str = ", ".join(flag_parts) if flag_parts else ""

        # Truncate next action
        next_str = (worst_next[:60] + "...") if len(worst_next) > 60 else worst_next

        # priority: from highest-priority backlog item
        priorities = [i.get("priority", "P3") for i in all_backlog]
        best_pri = min(priorities) if priorities else "—"

        table_rows.append([proj.name, best_pri, badge, str(len(proj.repos)), flags_str, next_str])

        # Rollup buckets
        if has_decision or worst_cls == "blocked-by-decision":
            needs_decision_names.append(proj.name)
        if worst_cls in ("stale", "dormant"):
            stale_names.append(proj.name)
        if worst_cls in ("clean-and-ready", "ready-for-next-agent"):
            clean_names.append(proj.name)

    projects_table = _table(
        ["Project", "Pri", "State", "Repos", "Flags", "Next action"],
        table_rows,
    )

    # --- rollup sections -----------------------------------------------------
    def _names_list(names: list[str]) -> str:
        if not names:
            return "_none_"
        return "\n".join(f"- [[Projects/{n}/Project Dashboard]]" for n in names)

    rollups = (
        f"## Needs decision\n\n{_names_list(needs_decision_names)}\n\n"
        f"## Stale\n\n{_names_list(stale_names)}\n\n"
        f"## Clean & ready\n\n{_names_list(clean_names)}"
    )

    # --- assemble -----------------------------------------------------------
    fm = _fm({
        "type": "creation-dashboard",
        "last_sync": now_iso,
        "generated_by": "creation_dashboards",
    })

    content = (
        f"{fm}\n\n"
        f"{_GENERATED_BANNER}\n\n"
        f"# Creation Dashboard\n\n"
        f"_Last sync: {now_iso}_\n\n"
        f"## ▶ Do next\n\n"
        + "\n".join(do_next_lines)
        + f"\n\n## Projects\n\n{projects_table}\n\n"
        + rollups
        + "\n\n---\n\n"
        "**Links:** [[Projects]] · [[Next Actions]] · [[Stale Context]] · "
        "[[Pending Decisions]] · [[Active Sessions]]\n"
    )

    path = vault_path / "Dashboard.md"
    rel = _safe_write(path, content, vault_path, dry_run)
    return {"path": rel, "dry_run": dry_run}


